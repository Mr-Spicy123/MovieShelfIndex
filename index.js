var movieDisplay = document.getElementById('id-display');
var resultsNumDisplay = document.getElementById('results-number');
var input = document.getElementById('movie-input');
input.addEventListener("input", function(event) {
    movieDisplay.innerHTML = '';
    var search = event.target.value.toLowerCase();

    for (let i = 0; i < movieContainers.length; i++) {
        for (let j = 0; j < movieContainers[i].length; j++) {
            console.log(movieContainers[i][j].title);
            if (movieContainers[i][j].title.toLowerCase().includes(search)) {
                var result = document.createElement('div');
                result.classList.add('search-result');
                var resultTitle = document.createElement('div');
                resultTitle.classList.add('result-title');
                resultTitle.innerHTML = "<b>" + movieContainers[i][j].title + "</b>";
                result.appendChild(resultTitle);
                var resultsLocation = document.createElement('div');
                resultsLocation.classList.add('results-location');
                resultsLocation.innerHTML = locationTags[i];
                result.appendChild(resultsLocation);
                movieDisplay.appendChild(result);
            }
        }
    }
    
    if (search === '') {
        movieDisplay.innerHTML = '';
    }

    var numResults = movieDisplay.querySelectorAll("div").length/3;
    resultsNumDisplay.innerHTML = "<b>" + numResults + " Results</b>";

    if (search === '') {
        resultsNumDisplay.innerHTML = '';
    }
});